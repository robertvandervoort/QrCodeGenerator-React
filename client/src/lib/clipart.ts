// Function to create SVG data URLs for the clipart icons with custom color
export const getClipArtDataUrl = (iconType: keyof typeof qrTypeIcons, color: string = '#000000'): string => {
  // Get the SVG string for the icon type
  const svgString = clipartSvgs[iconType] || clipartSvgs.url;
  
  // Replace the color with the custom color
  const coloredSvgString = svgString.replace(/stroke="currentColor"/g, `stroke="${color}"`);
  
  // Create a data URL from the SVG string
  return `data:image/svg+xml;base64,${btoa(coloredSvgString)}`;
};

// Convert a file to data URL
export const fileToDataUrl = async (file: File): Promise<string> => {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result as string);
    reader.onerror = reject;
    reader.readAsDataURL(file);
  });
};